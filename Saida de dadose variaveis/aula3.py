#OPERADORES RELACIONAIS
# maio:  >
# menor: <
# maior igual: >=
# menor igual: <=
# igual: ==
# diferente: !=
# inverte: Not
print("OPERADORES RELACIONAIS")
x = 10
y = 11

print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x == y)
print(x != y)
print(not x == y)

